public class Vehicle
{
	private String name;
	private int wheels;
	private int frill;
	

	public Vehicle(String name, int wheels, int frill)
	{
		this.name = name;
		this.wheels = wheels;
		this.frill = frill;
		
		
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getName()
	{
		return name;
	}

	public void setWheels(int wheels)
	{
		this.wheels = wheels;
	}

	public int getWheels()
	{
		return wheels;
	}

	public void setFrill(int frill)
	{
		this.frill = frill;
	}

	public int getFrill()
	{
		return frill;
	}

	
	public double move(){
		return move();
	}
}
