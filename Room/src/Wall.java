public class Wall
{
	private int heigth;
	private int width;

	public Wall(int heigth, int width)
	{
		
		this.heigth = heigth;
		this.width = width;
	}

	public void setHeigth(int heigth)
	{
		this.heigth = heigth;
	}

	public int getHeigth()
	{
		return heigth;
	}

	public void setWidth(int width)
	{
		this.width = width;
	}

	public int getWidth()
	{
		return width;
	}
	
}
